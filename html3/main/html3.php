                  <p class="text-center"><b>TRADITIONAL CHINESE FLOWER FESTIVALS </b></p>
                  <br>
                  <p class="text-justify">Anyone well versed in Chinese culture understands that China is a country steeped in millennium-old festivals and traditions. An example of this is the fact that what is arguably the most famous Chinese  festival of all – the Chinese New Year Festival, with its images of exploding firecrackers and prancing  dragons- is so old that historians are incapable of tracing its source.</p>
                  <br>
                  <p class="text-justify">Another traditional Chinese festival whose origins are only slightly less obscure is the traditional Flower  Festival, whose purpose is to help ease the transition from the colder and darker months of winter to  the warmer and sunnier months of spring. These festivals take their name, of course, from the beautiful flowers that decorate the festival surroundings, and that are immortalized in the traditional songs and dances that are performed on this special day. Due to the fact that China is such an immense territory, and that spring comes at different times of the year in different parts of the country, there is no one specific day when the Flower Festival is celebrated. However, it is safe to assume that most Chinese flower festivals take place sometime in mid-winter. </p>
                  <br>
                  <p class="text-justify">Historians are still in a quandary with regard to the date of the first Chinese Flower Festival. One group  of scholars believes that it took place sometime between the years 386 and 534 during the Northern Wei Dynasty. According to this version, the origin of the festival is connected to a young merchant named Nu Yi, who was well known for the magnificent flowers she grew and sold. So beautiful were her flowers that, following her death, she became known as the ‘Flower Goddess’, and the Festival evidently started as a way of honoring her. </p>
                  <br>
                  <p class="text-justify">Another group of scholars believes that the festival originated under the Tang Dynasty between the years 618 and 907. According to this account, the festival is associated with the name of Cui Xuanwei, who was an enlightened aristocrat of the time with a special passion for flowers. According to local legend, one night towards the end of winter, a flock of fairies descended upon Cui’s castle in order to talk to the sleeping flowers in his garden. The fairies told the flowers that a terrible storm was supposed to arrive the following day and that the flowers needed to wake up immediately and bloom before  morning’s first light or face the prospect of Spring being delayed another year.</p>
                  <br>
                  <p class="text-justify">The fairies also advised Cui to protect his garden with enormous strands of silk as a way of protecting  the soon-to-bloom flowers from the harsh winds of the approaching storm. Cui did as the fairies  instructed him, and the following day people came from near and far to admire the magnificent sight of Cui’s garden: thousands of flowers in full bloom, with their intoxicating fragrances, surrounded by reams of colorful silk. Since then, it has been a tradition for flower shops and flower enthusiasts throughout China to wrap their flowers in silk on the day of the Flower Festival.</p>
                  <br>
                  <p class="text-justify">One of the principal elements of the modern-day Chinese Flower Festival is a type of traditional  clothing worn by participants that is known as ‘hanfu’ and which dates from the Han Dynasty (202 B.C. to 220 A.D.) For men, the hanfu is characterized by long flowing robes  with loose sleeves and colorful  ‘kerchiefs’ or headpieces. The hanfu style for women is highlighted  by ‘chignons’ -- or long hair coiled  into a knot and fastened with jade hairpins -- which are accompanied by pearl earrings. </p>
                  <br>
                  <p class="text-justify">At the beginning of every Chinese Flower Festival, participants greet one another according to  elaborate rules of social conduct originating in the Han and Tang Dynasties. Following this, each  participant recites a poem focusing on the unique qualities of a different flower. Then, after a series of songs and games, the festival winds to a close with the performance of a traditional dance extolling the beauty and richness of China’s natural landscapes. The festival officially ends after participants hand out homemade flower cakes to observers. These deliciously light and fragrant desserts are  prepared according to an ancient recipe and represent a tasty ending to a ceremony that stimulates all of the senses. </p>
