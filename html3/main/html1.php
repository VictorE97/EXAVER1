                              <p><b>Paragraph 1 </b></p>
                              <p> These statues, carved from rough volcanic stone, take the form of stylized human heads on male torsos.  The heads are greatly exaggerated in size, with a three to five ratio between head and body. Strangely, some moai include what seem to be a four-foot base, known as “ahu”. Coincidentally, the term “ahu” is also used to refer to certain Easter Island burial grounds and other sacred ceremonial sites where several moai stand. </p>
